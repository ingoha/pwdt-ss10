package org.gello.model.sample;

import java.util.Date;
import java.util.List;

/**
 *
 * @author Administrator
 */
public class Patient {
    private String patientID;
    private String name;
    private String gender;
    private Date dob;
    private List<Allergy> allergies;
    private List<LabResult> labResults;
    private List<ProblemList> problems;
    private List<Medication> medications;
    
    /** Creates a new instance of Patient */
    public Patient() {
    }

    public String getPatientID() {
        return patientID;
    }

    public void setPatientID(String patientID) {
        this.patientID = patientID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public List<Allergy> getAllergies() {
        return allergies;
    }

    public void setAllergies(List<Allergy> allergies) {
        this.allergies = allergies;
    }

    public List<LabResult> getLabResults() {
        return labResults;
    }

    public void setLabResults(List<LabResult> labResults) {
        this.labResults = labResults;
    }

    public List<ProblemList> getProblems() {
        return problems;
    }

    public void setProblems(List<ProblemList> problems) {
        this.problems = problems;
    }

    public List<Medication> getMedications() {
        return medications;
    }

    public void setMedications(List<Medication> medications) {
        this.medications = medications;
    }
    
}
